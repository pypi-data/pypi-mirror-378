import re

def slugify_fa(s: str) -> str:
    """
    تبدیل متن فارسی/انگلیسی به اسلاگ ساده برای URL.
    مثال: "سلام دنیا! Django/Node" -> "salam-donya-django-node"
    """
    mapping = {
        "ا":"a","آ":"a","أ":"a","إ":"e","ب":"b","پ":"p","ت":"t","ث":"s","ج":"j","چ":"ch","ح":"h","خ":"kh",
        "د":"d","ذ":"z","ر":"r","ز":"z","ژ":"zh","س":"s","ش":"sh","ص":"s","ض":"z","ط":"t",
        "ظ":"z","ع":"a","غ":"gh","ف":"f","ق":"gh","ک":"k","ك":"k","گ":"g","ل":"l","م":"m","ن":"n",
        "و":"v","ه":"h","ی":"y","ي":"y","ء":"", "ٔ":"", "‌":"-", " ": "-",
    }
    s = "".join(mapping.get(ch, ch) for ch in s)
    s = re.sub(r"[^a-zA-Z0-9\-]+", "-", s)
    s = re.sub(r"-{2,}", "-", s).strip("-").lower()
    return s
