from enum import Enum

from pydantic import BaseModel


class IssuerCategory(Enum):
    FUND = "Fund"
    BANKING = "Banking"  # بانكي
    INSURANCE = "Insurance"  # بیمه
    MANUFACTURING = "Manufacturing"  # توليدي
    AGRICULTURE = "Agriculture"  # کشاورزی
    LEASING = "Leasing"  # ليزينگ
    INVESTMENT = "Investment"  # سرمايه گذاري
    SERVICE = "Service"  # خدماتي
    CONSTRUCTION = "Construction"  # ساختماني
    INVESTMENT_BANKING = "InvestmentBanking"  # تامین سرمایه


class IssuerDType(BaseModel):
    name: str
    symbol: str
    alias: str
    isin: str
    category: IssuerCategory


class Issuer:
    def __init__(self):
        self.issuers = [
            IssuerDType.model_validate(
                {
                    "symbol": i[0],
                    "name": i[1],
                    "alias": i[2],
                    "isin": i[3],
                    "category": i[4],
                }
            )
            for i in issuers
        ]

    def get_issuers_by_category(
        self, category: list[IssuerCategory]
    ) -> list[IssuerDType]:
        return [issuer for issuer in self.issuers if issuer.category in category]

    @staticmethod
    def normalize_symbol(w: str) -> str:
        dict_ = {
            "\u200f": "",
            "\u200c": "",
            "ي": "ی",
            "ك": "ک",
            "آ": "ا",
            " ": "",
            "‌": "",
        }
        return w.translate(str.maketrans(dict_))

    def validate(self, symbol: str) -> IssuerDType:
        item = next(
            filter(
                lambda x: self.normalize_symbol(x.symbol)
                == self.normalize_symbol(symbol),
                self.issuers,
            ),
            None,
        )
        if item == None:
            raise ValueError("Symbol not found or invalid or not supported yet.")
        return item


